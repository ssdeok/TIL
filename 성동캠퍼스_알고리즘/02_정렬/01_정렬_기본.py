# 오름차순 정렬

numbers = [1, 3, -4, 0, 100]
sorted_numbers = sorted(numbers)
print(numbers)
print(sorted_numbers)

numbers = [1, 3, -4, 0, 100]
numbers.sort()
print(numbers)

# 내림차순
numbers = [1, 3, -4, 0, 100]
sorted_numbers = sorted(numbers, reverse=True)
print(numbers)
print(sorted_numbers)

numbers = [1, 3, -4, 0, 100]
numbers.sort(reverse=True)
print(numbers)